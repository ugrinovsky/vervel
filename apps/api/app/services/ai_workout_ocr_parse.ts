/**
 * Разбор программы тренировки из текста (OCR / вставка текста).
 * Промпты для LLM и тупая нормализация пробелов/переносов перед вызовом модели.
 */
import { MUSCLE_ZONES } from '#services/ExerciseCatalog'

const ZONES_LIST = MUSCLE_ZONES.map((z) => `"${z}"`).join(',')

/**
 * Одна непустая строка — cleanup-LLM часто не нужен и может исказить короткий ввод.
 * Не извлекает упражнения, только решает «вызывать ли первый проход очистки».
 */
export function shouldSkipWorkoutProgramCleanup(input: string): boolean {
  const t = String(input ?? '').trim()
  if (t.length === 0 || t.length > 8000) return false
  const lines = t
    .split(/\r?\n/)
    .map((l) => l.trim())
    .filter((l) => l.length > 0)
  return lines.length <= 1
}

/** Системный промпт: коротко, без длинных примеров. */
export function workoutProgramParseSystemPrompt(): string {
  return `ПРИОРИТЕТ №1 — ИМЯ УПРАЖНЕНИЯ:
Во входном тексте название движения почти всегда стоит ПЕРЕД первым числом, которое относится к подходам (например перед «3 подхода», «3х12»).
Поля name и displayName = ТОЛЬКО это название (например «Румынская тяга»). Цифры подходов, повторов и кг в name НЕ ПИШИ.
Запрещено в name/displayName: «не указано», «неизвестно», «упражнение», «N/A» — если в тексте есть название движения, используй его.

ОБРАЗЕЦ (структура ответа для одной строки пользователя):
Ввод: румынская тяга 3 подхода на 12 раз по 36 кг
В exercises[0] обязательно: "name":"Румынская тяга","displayName":"Румынская тяга","sets":3,"setData":[{"reps":12,"weight":36},{"reps":12,"weight":36},{"reps":12,"weight":36}], плюс zones и zoneWeights по правилам ниже.

Разбери программу тренировки в один JSON-объект. Без markdown, без текста до/после, не массив.

Схема:
{"workoutType":"crossfit|bodybuilding|cardio","notes":"кратко по-русски","exercises":[{"name":"…","displayName":"…","sets":N,"reps":null,"weight":null,"duration":null,"notes":null,"supersetGroup":null,"setData":[{"reps":10,"weight":60}],"zones":["glutes","legs","back"],"zoneWeights":{"glutes":0.5,"legs":0.3,"back":0.2}}]}

Правила:
- weight — кг; duration — минуты. Нет в тексте → null. Не придумывай числа.
- РАЗДЕЛЕНИЕ НАЗВАНИЯ И ПАРАМЕТРОВ В ОДНОЙ СТРОКЕ (разговорный русский, опечатки допустимы):
  - Всё, что идёт ДО первого фрагмента с числом подходов/сетов — это name и displayName (название упражнения). Не включай в name цифры подходов, повторов и кг.
  - Шаблоны: «X подход(а|ов) на Y раз(а) по Z кг», «X подходов по Y повторов по Z кг», «X сетов на Y раз, Z кг», «на Y повторений по Z кг» — здесь X = число подходов, Y = reps, Z = weight (кг). Запятая в числе = десятичный разделитель (4,5 кг).
  - Пример: вход «румынская тяга 3 подхода на 12 раз по 36 кг» → один exercise: name/displayName «Румынская тяга» (нормализуй регистр и опечатки в названии движения), setData из трёх одинаковых элементов [{"reps":12,"weight":36},{"reps":12,"weight":36},{"reps":12,"weight":36}], sets=3.
  - Если вес один на все подходы — повтори тот же weight в каждом элементе setData; разные веса по строкам — разные weight в setData.
- Несколько строк подходов под ОДНИМ названием → один exercise; setData по строкам. «NxM, W кг» → N элементов с reps=M, weight=W (запятая в числе — десятичная).
- «3х15 каждой ногой» / «на каждую ногу» = 3 подхода по 15; вес на каждый подход, если указан.
- ДРОП-СЕТЫ (важно): если в названии упражнения есть «дроп сет»/«дроп-сет» и ниже перечислены несколько строк вида «4 кг на 10 раз», «3 кг на 12 раз», «2 кг на 15 раз», то это ступени внутри drop set.
  - Чтобы веса/повторы не терялись в приложении, РАСПЛЮЩИ drop set в обычные подходы в setData:
    - Каждая строка ступени = один элемент setData.
    - Если после ступеней есть "ROUNDS: N" / «N круга», то повтори весь набор ступеней N раз (в том же порядке) и получи setData длиной (ступени × N).
    - sets = длина setData.
    Пример: "4 кг на 10 раз / 3 кг на 12 / 2 кг на 15 / ROUNDS: 3"
    → sets=9, setData=[{10,4},{12,3},{15,2},{10,4},{12,3},{15,2},{10,4},{12,3},{15,2}]
  - В notes можно оставить пометку "Дроп-сет".
- Смешанная тренировка: есть силовые с весами + строка «Кардио …» → кардио в notes, не в exercises; workoutType не переключай на cardio из‑за этого.
- В тексте могут быть служебные строки:
  - "BLOCK: ..." или "HEADER: ..." — заголовок блока (НЕ упражнение; можно в notes)
  - "ROUNDS: N" — N полных кругов суперсета
- Суперсет:
  - строка "Суперсет" / "HEADER: Суперсет" — это заголовок, НИКОГДА не упражнение (не добавляй "Суперсет" в exercises)
  - каждое движение под заголовком — отдельный exercise (2 строки движений = 2 exercises)
  - если есть ROUNDS: N, то у КАЖДОГО упражнения этого суперсета ровно N записей в setData (один круг = один подход на упражнение)
- «Суперсет x N» / «Круговая x N» — заголовок: N подходов у каждого упражнения блока до следующего заголовка.
- «кг» и опечатка «кн» — килограммы.
- zones только из: [${ZONES_LIST}]. Перечисли все зоны с заметной нагрузкой и ни одной лишней: сколько уместно для движения (изоляция — часто 1 зона; базовые — несколько). Не дополняй список до «красивого» числа и не добавляй зону с пренебрежимым вкладом.
  - Это критично для аватара восстановления: точность важнее длины массива.
  - Не ставь zones «наугад»; если сомневаешься — только самые уместные зоны с реальным вкладом.
  - Примеры (ориентиры, не жёсткие лимиты):
    - Любая «румынская тяга» / hip hinge: zones включают "glutes" и "legs", часто также "back".
    - Любая «становая тяга»: обычно "glutes"+"legs"+"back".
    - Тяга штанги/гантели в наклоне: "back"+"biceps" (иногда "forearms").
    - Подтягивания: "back"+"biceps" (иногда "forearms").
    - Жим лёжа: "chests"+"triceps"+"shoulders".
    - Выпады/болгарские: "legs"+"glutes" (иногда "core").
- zoneWeights: ключи = ровно элементы zones; доли >0, сумма = 1; без нулей и без лишних ключей. Основной драйвер — большая доля; остальное по реальному вкладу. В каждом exercise вместе с zones.
- Строки протокола (AMRAP, EMOM, на время, табата N мин) — не exercises, только движения.
- displayName с заглавной; name/displayName по-русски, без транслита.
- КРИТИЧНО: name и displayName = слова упражнения ИЗ ВХОДНОГО ТЕКСТА (до цифр подходов/повторов), буквально скопируй или слегка нормализуй к каталогу («Румынская тяга»). Не подменяй на «неизвестно», «не указано», «упражнение» — это ломает приложение.
- ЗАПРЕЩЕНО подставлять общие заглушки вместо названия: не используй "Упражнение", "Движение", "Тренировка", "Неизвестно", "не указано", "Unknown", "N/A" как name/displayName, если в тексте есть конкретное движение.
- Короткая строка «Жим 3x8» / «румынка 3×10 60кг» — то же правило: name = слова ДО первого N×M или до «N подходов»; setData по смыслу.
- «Сгибания голени» в силовой программе = сгибания ног (бицепс бедра), не икры.
- «макс»/«до отказа» — reps условно ~12, вес не выдумывай.`
}

/** Первый проход: очистить OCR/вставленный текст от мусора, НЕ строя JSON. */
export function workoutProgramCleanupSystemPrompt(): string {
  return `Ты очищаешь текст программы тренировки перед разбором.

Верни ТОЛЬКО очищенный текст (plain text), без JSON, без markdown, без пояснений.

Цель: убрать явный мусор OCR и "шапки" приложений, сохранив структуру тренировки.

Правила:
- Сохраняй порядок строк и смысл.
- НЕ выдумывай упражнения, подходы, веса, минуты.
- НЕ меняй числа и единицы, кроме очевидной опечатки единицы веса: «кн» → «кг».
- Если вся программа в ОДНОЙ строке («румынская тяга 3 подхода…») — верни строку почти дословно; не сокращай название движения до «не указано» и не заменяй его служебными словами.
- НОРМАЛИЗУЙ СТРОКИ ПОДХОДОВ (чтобы второй шаг стабильно собрал setData):
  - если строка подхода имеет вид «NxM, W кг» (например «3x12, 35 кг»), разверни её в N строк вида «1xM, W кг» (три строки подряд).
  - если строка подхода имеет вид «NxM» без веса — разверни в N строк вида «1xM».
  - это НЕ новое упражнение, это те же подходы; числа не меняй.
- Удаляй строки, которые явно НЕ относятся к программе тренировки, например:
  - время/проценты/статусы вроде «16:03», «19%»
  - почта/логины/доменные строки (с символом @)
  - названия приложений/разделов («Мой календарь», «Календарь», и т.п.)
  - одиночные служебные слова/заголовки без смысла для тренировки
- Нумерацию в начале строки («1. », «2) ») можно убрать, если она мешает, но сам текст строки оставь.
- ВАЖНО ПРО СУПЕРСЕТЫ:
  - если встречаешь строку «Суперсет» (или похожий заголовок), оставь её, но выведи как "HEADER: Суперсет"
  - если встречаешь строку вида «3 круга» — выведи как "ROUNDS: 3" (это НЕ упражнение)
- Пустые строки допускаются для разделения упражнений/блоков.`
}

export function normalizeWorkoutTextForParsing(input: string): string {
  const raw = String(input ?? '')
    .replace(/\r\n/g, '\n')
    .replace(/\r/g, '\n')
    .normalize('NFKC')

  const lines = raw.split('\n').map((l) =>
    l
      .replace(/\t/g, ' ')
      .replace(/[ ]{2,}/g, ' ')
      .trim()
  )

  const out: string[] = []
  for (const line of lines) {
    if (!line) {
      if (out.length && out[out.length - 1] !== '') out.push('')
      continue
    }

    // Intentionally dumb: no semantic normalization here.
    // All content cleanup / unit fixing / set-line expansion happens in the LLM cleanup step.
    out.push(line)
  }

  while (out.length && out[0] === '') out.shift()
  while (out.length && out[out.length - 1] === '') out.pop()

  return out.join('\n')
}
